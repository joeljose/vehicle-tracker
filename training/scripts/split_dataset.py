#!/usr/bin/env python3
"""Create stratified train/val/test splits for YOLO fine-tuning.

Iterates all dedup frames, pairs each with its RF-DETR auto-label
(from data/labels/{accepted,flagged}/ or an empty .txt for frames where
the teacher found nothing), stratifies by site, splits 70/15/15 with a
fixed seed, and materializes the three splits as symlink trees under
`data/splits/`. Writes `data/dataset.yaml` at the end.

Layout created:

    data/splits/
        train/
            images/junction_741_73/frame_000001.jpg   (symlink)
            labels/junction_741_73/frame_000001.txt   (symlink or empty)
        val/
            images/...
            labels/...
        test/
            images/...
            labels/...
    data/dataset.yaml

Ultralytics' YOLO training finds labels by substituting `images/` -> `labels/`
in the image path, so the sibling-directory layout is mandatory.
"""

from __future__ import annotations

import random
import shutil
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).parent))

from _common import REPO_ROOT, load_video_sites, log  # noqa: E402

DEDUP_ROOT = REPO_ROOT / "data" / "frames_dedup"
LABELS_ROOT = REPO_ROOT / "data" / "labels"
SPLITS_ROOT = REPO_ROOT / "data" / "splits"
DATASET_YAML = REPO_ROOT / "data" / "dataset.yaml"

TRAIN_FRAC = 0.70
VAL_FRAC = 0.15
# test_frac = 0.15 (remainder)

SEED = 0


def find_label_file(slug: str, stem: str) -> Path | None:
    """Return the RF-DETR auto-label path for a frame, or None if empty."""
    for kind in ("accepted", "flagged"):
        cand = LABELS_ROOT / kind / slug / f"{stem}.txt"
        if cand.exists():
            return cand
    return None


def stratified_split(
    site_frames: dict[str, list[str]],
) -> tuple[dict[str, list[tuple[str, str]]], dict[str, dict[str, int]]]:
    """Returns (splits, per_site_stats).

    splits[split_name] is a list of (site_slug, frame_stem) tuples.
    per_site_stats[site][split_name] is the count of frames in that bucket.
    """
    rng = random.Random(SEED)
    splits: dict[str, list[tuple[str, str]]] = {
        "train": [],
        "val": [],
        "test": [],
    }
    per_site: dict[str, dict[str, int]] = {}

    for site, stems in site_frames.items():
        ordered = sorted(stems)  # deterministic starting point
        rng.shuffle(ordered)     # deterministic shuffle from SEED
        n = len(ordered)
        n_train = int(n * TRAIN_FRAC)
        n_val = int(n * VAL_FRAC)

        train = ordered[:n_train]
        val = ordered[n_train : n_train + n_val]
        test = ordered[n_train + n_val :]

        splits["train"].extend((site, s) for s in train)
        splits["val"].extend((site, s) for s in val)
        splits["test"].extend((site, s) for s in test)

        per_site[site] = {
            "train": len(train),
            "val": len(val),
            "test": len(test),
            "total": n,
        }
    return splits, per_site


def create_split_symlinks(
    frames: list[tuple[str, str]], split_name: str
) -> tuple[int, int, int]:
    """Materialize one split as symlink trees. Returns (n_images, n_labeled, n_empty)."""
    images_root = SPLITS_ROOT / split_name / "images"
    labels_root = SPLITS_ROOT / split_name / "labels"
    images_root.mkdir(parents=True, exist_ok=True)
    labels_root.mkdir(parents=True, exist_ok=True)

    n_images = 0
    n_labeled = 0
    n_empty = 0

    for site, stem in frames:
        # Image: symlink to the real file in data/frames_dedup/
        img_src = (DEDUP_ROOT / site / f"{stem}.jpg").resolve()
        img_dst_dir = images_root / site
        img_dst_dir.mkdir(parents=True, exist_ok=True)
        img_dst = img_dst_dir / f"{stem}.jpg"
        if img_dst.is_symlink() or img_dst.exists():
            img_dst.unlink()
        img_dst.symlink_to(img_src)
        n_images += 1

        # Label: symlink to the real auto-label, or write an empty .txt for
        # frames where RF-DETR found nothing (so YOLO sees them as valid
        # background-only training examples, not missing-label errors).
        label_src = find_label_file(site, stem)
        label_dst_dir = labels_root / site
        label_dst_dir.mkdir(parents=True, exist_ok=True)
        label_dst = label_dst_dir / f"{stem}.txt"
        if label_dst.is_symlink() or label_dst.exists():
            label_dst.unlink()
        if label_src is not None:
            label_dst.symlink_to(label_src.resolve())
            n_labeled += 1
        else:
            label_dst.write_text("")
            n_empty += 1

    return n_images, n_labeled, n_empty


def write_dataset_yaml() -> None:
    yaml_content = """# Ultralytics dataset config for M8-P1.5 v2 single-class fine-tune.
# Auto-generated by scripts/split_dataset.py — do not edit by hand.
# Paths are absolute to the path INSIDE the training docker container.

path: /app/data/splits
train: train/images
val: val/images
test: test/images

names:
  0: vehicle
"""
    DATASET_YAML.write_text(yaml_content)


def main() -> None:
    if SPLITS_ROOT.exists():
        shutil.rmtree(SPLITS_ROOT)
        log(f"Wiped existing {SPLITS_ROOT}")

    sites = load_video_sites()
    site_frames: dict[str, list[str]] = {}
    for slug in sites:
        site_dir = DEDUP_ROOT / slug
        if not site_dir.exists():
            log(f"  [warn] {slug}: dedup dir missing, skipping")
            continue
        stems = [p.stem for p in site_dir.glob("frame_*.jpg")]
        site_frames[slug] = stems
        log(f"  {slug}: {len(stems)} frames in dedup pool")

    total = sum(len(v) for v in site_frames.values())
    log(f"Total frames: {total}")

    splits, per_site_stats = stratified_split(site_frames)
    log("")
    log("Split totals:")
    for name in ("train", "val", "test"):
        log(f"  {name}: {len(splits[name])}")
    log("")
    log("Per-site stratification:")
    for site, s in per_site_stats.items():
        log(
            f"  {site:<30} "
            f"train={s['train']:>5} val={s['val']:>4} test={s['test']:>4} "
            f"(total={s['total']})"
        )

    log("")
    log("Materializing symlink trees under data/splits/...")
    for name in ("train", "val", "test"):
        n_img, n_lab, n_empty = create_split_symlinks(splits[name], name)
        log(f"  {name}: {n_img} images, {n_lab} labeled, {n_empty} empty-label")

    write_dataset_yaml()
    log(f"Wrote {DATASET_YAML}")
    log("Done.")


if __name__ == "__main__":
    main()
